package com.maternite.gestion.mesobjets;

public class BeansInterventions {

    String dates, dents, interventions;

    public BeansInterventions() {
    }

    public String getDates() {
        return dates;
    }

    public void setDates(String dates) {
        this.dates = dates;
    }

    public String getDents() {
        return dents;
    }

    public void setDents(String dents) {
        this.dents = dents;
    }

    public String getInterventions() {
        return interventions;
    }

    public void setInterventions(String interventions) {
        this.interventions = interventions;
    }
}
